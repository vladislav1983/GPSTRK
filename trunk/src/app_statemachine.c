typedef enum teAppState
{
    eAPP_STATE_INIT = 0,
    eAPP_NMEA_MSG_POOL,
    eAPP_SD_CARD_WRITE_POSITION,
    eApp_APRS_TRANSMIT,
    eAPP_WAIT_STATE


}tAppState;

