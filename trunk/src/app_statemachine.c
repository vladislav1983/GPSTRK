/*=====================================================================================================================
 * Parameters: void
 *
 * Return: void
 *
 * Description: 
 *===================================================================================================================*/
void __attribute__((user_init)) App_Init(void)
{
    AppState = eAPP_STATE_INIT;
    AppStateAlarm.TaskID = cAppStatemachineTaskId;
    OsSetAlarm(&AppStateAlarm, (cAppStateMachineTaskPeriodUs/cOsAlarmTickUs));
    u16SYSTEM_FLAGS = 0;
}

/*=====================================================================================================================
 * Parameters: void
 *
 * Return: void
 *
 * Description: 
 *===================================================================================================================*/
void App_StatemachineTask(void)
{

    switch(AppState)
    {
    //------------------------------------------------------------------------------------------------------------------
    case eAPP_STATE_INIT:

        OSStartTimer(&AppStateTimer);
        // wait 1s before configuration load
        WaitTimeTicks = 1000UL/cOsTimerTick_ms;
        AppState = eAPP_WAIT_STATE;
        AppNextState = eApp_APRS_TRANSMIT;

        break;
    //------------------------------------------------------------------------------------------------------------------
    case eApp_APRS_TRANSMIT:

        Aprs_Transmit(0);

        OSStartTimer(&AppStateTimer);
        WaitTimeTicks = 5000UL/cOsTimerTick_ms;
        AppState = eAPP_WAIT_STATE;
        AppNextState = eApp_APRS_TRANSMIT;

        break;
    //------------------------------------------------------------------------------------------------------------------
    case eAPP_WAIT_STATE:

        _Assert(AppState != AppNextState);

        if(cFalse != OSIsTimerElapsed(&AppStateTimer, WaitTimeTicks))
        {
            AppState = AppNextState;
        }

        break;
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    default:
        _Assert(cFalse);
        break;
    }


}



