/*=====================================================================================================================
 * Local types
 *===================================================================================================================*/
typedef enum teAppState
{
    eAPP_STATE_INIT = 0,
    eAPP_SD_CARD_LOAD_CONFIGURATION,
    eApp_APRS_TRANSMIT,
    eAPP_WAIT_STATE


}tAppState;

/*=====================================================================================================================
 * Local data
 *===================================================================================================================*/
static tAppState AppState;
static tAppState AppNextState;
static tOSAlarm  AppStateAlarm;
static tOSTimer  AppStateTimer;
static tOSTimer WaitTimeTicks;


/*=====================================================================================================================
 * Constant Local Data
 *===================================================================================================================*/

